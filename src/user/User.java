public class User {

    private Integer matricula;
    private String nome;
    private String sobrenmome;
    private String email;
    private String endereco;
    private int idade;
    private String rest_med;
    private double num_tel;
    private double peso;
    private double altura;
    private Integer cod_plan;
    private String senha;

    public Integer getMatricula() {
        return matricula;
    }

    public void setMatricula(Integer matricula) {
        this.matricula = matricula;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSobrenmome() {
        return sobrenmome;
    }

    public void setSobrenmome(String sobrenmome) {
        this.sobrenmome = sobrenmome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getRest_med() {
        return rest_med;
    }

    public void setRest_med(String rest_med) {
        this.rest_med = rest_med;
    }

    public double getNum_tel() {
        return num_tel;
    }

    public void setNum_tel(double num_tel) {
        this.num_tel = num_tel;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }

    public double getAltura() {
        return altura;
    }

    public void setAltura(double altura) {
        this.altura = altura;
    }

    public Integer getCod_plan() {
        return cod_plan;
    }

    public void setCod_plan(Integer cod_plan) {
        this.cod_plan = cod_plan;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

}
