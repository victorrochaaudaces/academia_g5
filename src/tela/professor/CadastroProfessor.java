    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (professor == null) {
            professor = new Professor();
            professor.setNmProf(varNomeProf.getText().trim());
            professor.setArea(varArea.getText().trim());
            funcionario.setId(id);
            professor.setFuncionario(funcionario);
        } else {
            professor.setNmProf(varNomeProf.getText().trim());
            professor.setArea(varArea.getText().trim());
            funcionario.setId(id);
            professor.setFuncionario(funcionario);
            professor.setCodProf(id_prof);
        }
        try {
            if (professor.getCodProf() == null) {
                professorDao.salvar(professor);
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!!");
            } else {
                professorDao.alterar(professor);
                JOptionPane.showMessageDialog(null, "Alterado com sucesso!!");
            }
            new ProfessoresHome().setVisible(true);
            dispose();
        } catch (Exception e) {
            System.out.println("Erro ao salvar ou alterar os dados do professor " + e.getMessage());
        }
    }//GEN-LAST:event_btSalvarActionPerformed

